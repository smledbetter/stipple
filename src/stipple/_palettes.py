_BUILTIN_CONTINUOUS = {
    "viridis": VIRIDIS_RGB,
    "plasma": PLASMA_RGB,
    "magma": MAGMA_RGB,
    "inferno": INFERNO_RGB,
}

_BUILTIN_CATEGORICAL = {
    "tab10": TAB10_RGB,
}


def resolve_palette(name_or_array: Any, *, want_continuous: bool) -> np.ndarray:
    """Return a palette as an (N, 4) float32 RGBA numpy array.

    Accepts:
      - a built-in name string ("viridis", "plasma", "magma", "inferno",
        "tab10", or any name `matplotlib.colormaps` recognises if mpl is
        installed)
      - a numpy / array-like of shape (N, 3) or (N, 4) in [0, 1]
    """
    if isinstance(name_or_array, str):
        name = name_or_array.lower()
        # Built-ins first — no mpl required
        for source in (_BUILTIN_CONTINUOUS, _BUILTIN_CATEGORICAL):
            if name in source:
                rgb = np.asarray(source[name], dtype=np.float32)
                return _add_alpha(rgb)
        # Fall back to matplotlib if available
        try:
            from matplotlib import colormaps as mpl_cmaps  # type: ignore[import-not-found]
        except ImportError as e:
            raise ValueError(
                f"Unknown built-in palette {name!r}. Built-ins: "
                f"{sorted(set(_BUILTIN_CONTINUOUS) | set(_BUILTIN_CATEGORICAL))}. "
                f"Install matplotlib for full colormap support, or pass an (N, 3) numpy array."
            ) from e
        cm = mpl_cmaps[name]
        n = 256 if want_continuous else max(2, getattr(cm, "N", 256))
        rgba = np.array([cm(i / max(1, n - 1)) for i in range(n)], dtype=np.float32)
        return rgba

    arr = np.asarray(name_or_array, dtype=np.float32)
    if arr.ndim != 2 or arr.shape[1] not in (3, 4):
        raise ValueError(f"Custom palette must be (N, 3) or (N, 4); got {arr.shape}.")
    return _add_alpha(arr)


def _add_alpha(rgb: np.ndarray) -> np.ndarray:
    if rgb.shape[1] == 4:
        return rgb
    rgba = np.empty((rgb.shape[0], 4), dtype=np.float32)
    rgba[:, :3] = rgb
    rgba[:, 3] = 1.0
    return rgba

